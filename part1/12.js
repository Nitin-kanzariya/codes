// == vs === 

// == (Equality Operator):It checks for value equality, but not necessarily type equality.
1 == '1'; // true because the string '1' is converted to the number 1 before comparison


//=== (Strict Equality Operator):It checks for both value equality and data type equality.
1 === '1'; // false because the types are different (number vs. string)


